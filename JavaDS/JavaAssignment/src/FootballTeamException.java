
public class FootballTeamException extends Exception
{

   public FootballTeamException ()
   {
      super ();
   }

   public FootballTeamException (String message)
   {
      super (message);
   }

   public FootballTeamException (Throwable cause)
   {
      super (cause);
   }

   public FootballTeamException (String message, Throwable cause)
   {
      super (message, cause);
   }
}